// Player starts at bottom-left available path cell
export function findPlayerStart(maze) {
  for (let r = maze.length - 1; r >= 0; r--)
    for (let c = 0; c < maze[0].length; c++)
      if (maze[r][c] === 0) return { r, c };
  return { r: maze.length - 1, c: 0 };
}

// Villain starts at center of maze; if center is a wall, shift right then up until path found
export function findVillainStart(maze) {
  const rows = maze.length;
  const cols = maze[0].length;
  let cr = Math.floor(rows / 2);
  let cc = Math.floor(cols / 2);
  // Spiral out: try right first, then up, expanding search
  if (maze[cr][cc] === 0) return { r: cr, c: cc };
  for (let dist = 1; dist < Math.max(rows, cols); dist++) {
    for (let dc = 0; dc <= dist; dc++) {
      for (let dr = 0; dr <= dist; dr++) {
        // Try right-then-up bias as per spec
        const tries = [
          [cr - dr, cc + dc], [cr - dr, cc - dc],
          [cr + dr, cc + dc], [cr + dr, cc - dc],
        ];
        for (const [tr, tc] of tries) {
          if (tr >= 0 && tr < rows && tc >= 0 && tc < cols && maze[tr][tc] === 0)
            return { r: tr, c: tc };
        }
      }
    }
  }
  return { r: cr, c: cc };
}
